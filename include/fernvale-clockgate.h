#define CLKGATE_MODEMGSM_CG_CLR2          (0x83000000+0x0018)
#define CLKGATE_MODEMGSM_CG_CLR4          (0x83000000+0x0098)

#define MODEMGSM_CG_CTL2_TDMA     0x0001
